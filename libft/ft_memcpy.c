#include <stddef.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*tmp_dest;
	char	*tmp_src;

	tmp_dest = (char *)dest;
	tmp_src = (char *)src;
	if (dest == src)
		return (dest);
	while (n-- > 0)
		*tmp_dest++ = *tmp_src++;
	return (dest);
}
